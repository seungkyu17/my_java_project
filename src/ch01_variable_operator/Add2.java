package ch01_variable_operator;

public class Add2 {
    public static void main(String[] args) {
        int a ;  
        int b ;
        int c ;
        int result ;

        a = 3 ;
        b = 4 ;
        c = 5 ;
        result = 2*a + 3*b - c ;

        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("result = " + result);
    }
}
